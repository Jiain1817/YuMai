  /// 统一翻译方法
  /// [lang] 当前语言代码：zh / bo / ii
  /// [section] 翻译分区：common / home / storyList / profile
  /// [key] 翻译键名
  /// [params] 可选参数，用于替换字符串中的占位符（如 {count}）
  static String t(String lang, String section, String key, [Map<String, String>? params]) {
    final sectionMap = _getSection(section);
    final langMap = sectionMap[lang] ?? sectionMap['zh']!;
    String result = langMap[key] ?? sectionMap['zh']![key] ?? key;

    if (params != null) {
      params.forEach((placeholder, value) {
        result = result.replaceAll('{$placeholder}', value);
      });
    }
    return result;
  }

  static Map<String, Map<String, String>> _getSection(String section) {
    switch (section) {
      case 'common':
        return common;
      case 'home':
        return home;
      case 'storyList':
        return storyList;
      case 'profile':
        return profile;
      case 'storyDetail':
        return storyDetail;
      case 'bottomNav':
        return bottomNav;
      default:
        return common;
    }
  }
